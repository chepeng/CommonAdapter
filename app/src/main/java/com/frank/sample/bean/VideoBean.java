package com.frank.sample.bean;

public class VideoBean {
    private String video_logo_url;
    private String name;
    private String desc;

    public String getVideo_logo_url() {
        return video_logo_url;
    }

    public void setVideo_logo_url(String video_logo_url) {
        this.video_logo_url = video_logo_url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
